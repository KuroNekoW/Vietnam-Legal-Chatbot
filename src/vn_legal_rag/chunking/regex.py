from __future__ import annotations

import re

# ==========================================================
# Article (Điều)
# ==========================================================

ARTICLE_PATTERN = re.compile(
    r"(?m)^Điều\s+(\d+)\.\s*(.+)$"
)

# ==========================================================
# Clause (Khoản)
# ==========================================================

CLAUSE_PATTERN = re.compile(
    r"(?m)^(\d+)\.\s*"
)

# ==========================================================
# Point (Điểm)
# ==========================================================

POINT_PATTERN = re.compile(
    r"(?m)^([a-zđ])\)\s*"
)

